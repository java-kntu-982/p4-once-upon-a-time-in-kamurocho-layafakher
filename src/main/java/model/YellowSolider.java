package model;

public class YellowSolider extends EnemySolider{

    public YellowSolider(double health, double attack, Integer attackRange) {
        super(health, attack, attackRange);
    }

    @Override
    public boolean isAlive() {
        return super.isAlive();
    }

    @Override
    public void setAlive(boolean alive) {
        super.setAlive(alive);
    }

    @Override
    public double getHealth() {
        return super.getHealth();
    }

    @Override
    public void setHealth(double health) {
        super.setHealth(health);
    }

    @Override
    public double getAttack() {
        return super.getAttack();
    }

    @Override
    public void setAttack(double attack) {
        super.setAttack(attack);
    }

    @Override
    public Integer getAttackRange() {
        return super.getAttackRange();
    }

    @Override
    public void setAttackRange(Integer attackRange) {
        super.setAttackRange(attackRange);
    }

    @Override
    public double getX() {
        return super.getX();
    }

    @Override
    public void setX(double x) {
        super.setX(x);
    }

    @Override
    public double getY() {
        return super.getY();
    }

    @Override
    public void setY(double y) {
        super.setY(y);
    }

    @Override
    public double getXSpeed() {
        return super.getXSpeed();
    }

    @Override
    public void setXSpeed(double XSpeed) {
        super.setXSpeed(XSpeed);
    }

    @Override
    public double getYSpeed() {
        return super.getYSpeed();
    }

    @Override
    public void setYSpeed(double YSpeed) {
        super.setYSpeed(YSpeed);
    }
}
